from gtts import gTTS
import os

# 1분 분량의 강의 대본
text = """
안녕하세요, 여러분. 지난 시간에는 인공지능의 역사에 대해 간략히 살펴봤었죠? 
오늘은 머신러닝의 가장 핵심적인 두 가지 개념, 바로 지도 학습과 비지도 학습의 차이에 대해 본격적으로 이야기해보겠습니다.

쉽게 비유하자면, 지도 학습은 정답지가 있는 문제집을 푸는 것과 같습니다. 
데이터에 라벨이라는 정답이 붙어 있어서, 컴퓨터가 이걸 보며 학습을 하죠. 
스팸 메일 필터가 대표적인 예입니다.

반면에 비지도 학습은 정답 없이 데이터 자체의 숨겨진 패턴을 찾아내는 과정입니다. 
아이에게 정답을 알려주지 않아도 강아지와 고양이를 구별하게 되는 원리와 비슷하죠. 
마케팅에서 비슷한 성향의 고객들을 그룹으로 묶는 군집화가 여기에 해당합니다.

자, 그럼 이론은 여기까지 하고 이 두 가지가 실제로 어떻게 코드로 구현되는지 예제를 통해 살펴볼까요?
"""

# Google TTS를 사용하여 한국어(ko)로 변환
tts = gTTS(text=text, lang='ko', slow=False)

# mp3 파일로 저장
filename = "lecture_sample.mp3"
tts.save(filename)

print(f"'{filename}' 파일이 생성되었습니다. 재생해보세요!")

# (Windows인 경우) 파일 자동 실행
os.system(f"start {filename}")